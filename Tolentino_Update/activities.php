		<p><strong>Problem:</strong></p>
		<p>Smartphones tend to be distractive while learning because of the connectivity it offers. The presence of smartphones affect the focus a student as one tries to check notifications to stay connected from time to time.</p>
		<p><strong>App Solution:</strong></p>
		<p>Our group came up with an app idea solution for this problem which is, Study Lock. We find it very helpful to have this kind of app because all of us review our lecture notes in our mobile phones because it’s easier and paperless. Our app’s main feature is the complete lock, which will lock the phone from all of its features excluding emergency call features and partial lock, for selected apps only, for span of time set by the user. Both will be very effective as most people tend to still check their phones even without notification or any alerts receive. This will allow users to have undivided focus during study sessions.</p>
		<p><strong>Supporting Studies:</strong></p>
		<li>Researchers found that students sending and receiving messages while studying scored lower test results and were less effective at tasks such as note taking. It found that when students did not use mobiles, they were better at being able to recall information. (BBC)</li>
		<li>Cell phones give students access to tools and apps that can help them complete and stay on top of their class work. These tools can also teach students to develop better study habits, like time management and organization skills. (Oxford Learning)</li>
